
// Этап 2: Определение типов данных
// В TypeScript мы заранее описываем, как выглядят наши данные.
// Это помогает избежать ошибок и делает код более понятным.
// Здесь мы описываем, что такое "Видео" в нашей системе.

export interface Video {
  id: string; // Уникальный идентификатор, например 'video-168934…'
  name: string; // Название, которое вы вводите, например "Мое первое видео"
  url: string; // Ссылка на видеофайл на облачном хостинге
  isActive: boolean; // Включена ли трансляция (true/false)
  createdAt: number; // Время создания, для сортировки
}

// Определяем возможные языки для локализации
export type Language = 'en' | 'ru' | 'he';
